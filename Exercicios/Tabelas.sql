CREATE TABLE MAQUINA (
IDMAQUINA INT PRIMARY KEY NOT NULL,
TIPO VARCHAR(255),
VELOCIDADE INT,
HARDDISK INT,
PLACA_REDE INT,
MEMORIA_RAM INT,
FK_USUARIO INT,
FOREIGN KEY(FK_USUARIO) REFERENCES USUARIOS(IDUSUARIO)
);

CREATE TABLE USUARIOS (
IDUSUARIO INT PRIMARY KEY NOT NULL,
SENHA VARCHAR(255),
NOMEUSER VARCHAR(255),
RAMAL INT,
ESPECIALIDADE VARCHAR(255)
);

CREATE TABLE SOFTWARE (
IDSOFTWARE INT PRIMARY KEY NOT NULL,
PRODUTO VARCHAR(255),
HARDDISK INT,
MEMORIA_RAM INT,
FK_MAQUINA INT,
FOREIGN KEY(FK_MAQUINA) REFERENCES MAQUINA(IDMAQUINA)
);
